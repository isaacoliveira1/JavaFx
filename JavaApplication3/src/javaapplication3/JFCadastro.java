   private void buttonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisterActionPerformed
      // TODO add your handling code here:

      JIFPrivate Cliente = new JIFPrivate();
      if(!"".equals(txtCodigo.getText())){
         Cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
      }else{
         txtCodigo.requestFocus();
      }
      if(!"".equals(txtNome.getText())){
               Cliente.setNome(txtNome.getText());

      } else {
                  txtNome.requestFocus();

      }
      if(!"".equals(txtCPF.getText())){
               Cliente.setCPF(txtCPF.getText());
      } else{
                  txtCPF.requestFocus();

      }
      if (!"".equals(txtCidade.getText())){
               Cliente.setCidade(txtCidade.getText());
      } else {
               txtCidade.requestFocus();
      }
      if (!"".equals(optionUF.getSelectedItem().toString())){
        Cliente.setUF(optionUF.getSelectedItem().toString());
      } else{
                  optionUF.requestFocus();
      }
            System.out.println("" + Cliente);
      JOptionPane.showMessageDialog(rootPane, "Conta criada", "Suceso!", 1);
      dispose();


   }//GEN-LAST:event_buttonRegisterActionPerformed

